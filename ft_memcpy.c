#include "libft.h"

void			*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t		i;
	char		*d;
	const char	*s;

	i = 0;
	d = dst;
	s = src;
	if (n > 0)
	{
		while (n > i)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (dst);
}
